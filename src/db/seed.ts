// ─── Función principal de seed ────────────────────────────────────────────────

export async function cargarSeed(): Promise<void> {
  const ahora = new Date()

  // 1. Insertar categorías y obtener sus IDs reales
  const categoriaIds: number[] = []
  for (const cat of CATEGORIAS) {
    const id = await db.categorias.add(cat)
    categoriaIds.push(id as number)
  }

  // 2. Insertar todos los productos con fechas y campos por defecto
  const productosConFechas = PRODUCTOS_SEED.map((prod) => ({
    ...prod,
    // El categoriaId del seed corresponde al índice en CATEGORIAS (1-based)
    // que coincide con el orden de inserción, así que los IDs son consecutivos.
    esFantasma: false,
    activo: true,
    creadoEn: ahora,
    actualizadoEn: ahora,
  }))

  await db.productos.bulkAdd(productosConFechas)

  // 3. Insertar configuración por defecto de la tienda
  await db.configTienda.put(CONFIG_DEFAULT)
}

// ─── Estadísticas del seed (para verificación) ───────────────────────────────

export function contarProductosPorCategoria() {
  const conteo: Record<number, number> = {}
  for (const p of PRODUCTOS_SEED) {
    conteo[p.categoriaId] = (conteo[p.categoriaId] ?? 0) + 1
  }
  return conteo
}

